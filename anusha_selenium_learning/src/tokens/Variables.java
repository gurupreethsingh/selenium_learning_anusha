package tokens;

public class Variables {

	public static void main(String[] args) 
	{
		short s =  23; 
		int a = 10;
		float f  = 5.4f; 
		double d = 3.64564;
		long l = 896986798698l;
		System.out.println(a);
		System.out.println(f);
		System.out.println(d);
		System.out.println(s);
		System.out.println(l);
	}
}
